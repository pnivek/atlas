// Apply 128-element signs to a per-warp [8] vec (32 threads × 4 elements per thread, lane * 4 + i offsets).
__device__ __forceinline__ void apply_signs_128_pre(float vals[4], unsigned int lane) {
    #pragma unroll
    for (int i = 0; i < 4; i++) {
        unsigned int idx = lane * 4 + i;
        vals[i] *= TQP_SIGNS1_128[idx];
    }
}

__device__ __forceinline__ void apply_signs_128_post(float vals[4], unsigned int lane) {
    #pragma unroll
    for (int i = 0; i < 4; i++) {
        unsigned int idx = lane * 4 + i;
        vals[i] *= TQP_SIGNS2_128[idx];
    }
}


