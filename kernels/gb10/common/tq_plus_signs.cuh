//
// TurboQuant+ Rademacher sign arrays (seed=42).
//
// See CITATIONS.md for prior-art chain.
//
// Why two-sided signs?
//   Plain WHT (Hadamard-only) does NOT Gaussianize arbitrary input distributions.
//   Per Google ICLR 2026 TurboQuant: apply Rademacher signs BEFORE and AFTER the
//   butterfly to give Lindeberg-CLT-driven outlier elimination on any input dist.
//   Without signs, WHT just unitary-rotates — you keep outlier mass on principal
//   directions, which clips in FP8 and wastes dynamic range in low-bit codebooks.
//
// Inverse rotation: swap signs1/signs2 (matrix is its own transpose up to sign order).

